#include "minishell.h"

void	ft_strcpy(char *dst, const char *src)
{
	size_t	i;

	if (!src)
		return ;
	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
}
