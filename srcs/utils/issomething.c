#include "minishell.h"

int	ft_isspace(int c)
{
	return (c == '\t'
		|| c == '\n'
		|| c == '\v'
		|| c == '\f'
		|| c == '\r'
		|| c == ' ');
}

int	ft_isasep(const char c)
{
	return (c == '\''
		|| c == '"'
		|| c == '<'
		|| c == '>'
		|| c == '|'
		|| c == '('
		|| c == ')');
}
